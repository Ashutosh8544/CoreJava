package com.nt.pojo;

class Student{
	private int sno;
	private String sName;
	private String course;

	public void setSno(int sno){
		this.sno = sno;
	}
	public int getSno(){
		return sno;
	}
	
	public void setSName(String sName){
		this.sName = sName;
	}
	public String getSname(){
		return sName;
	}

	public void setCourse(String course){
		this.course = course;
	}
	public String getCourse(){
		return course;
	}


}
