class AUB{
	public static void main(String[] args) 
	{
		//Auto unboxing
		Byte				b			 =			 new Byte((byte)40);			System.out.println(b);
		Short				s			 =			 new Short((short)50);		System.out.println(s);
		Integer			i			 =          new Integer(60);					System.out.println(i);
		Long				l			 =			 new Long(70L);					System.out.println(l);
		Float				f			 =			 new Float(80F);					System.out.println(f);
		Double			d			 =			 new Double(90D);				System.out.println(d);
		Character   ch			 =			 new Character('a');			System.out.println(ch);
		Boolean	   bo		 =		    new Boolean(true);			System.out.println(bo);
	}
}